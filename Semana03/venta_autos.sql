-- tabla: MARCA

CREATE TABLE MARCA(
	IDMARCA INT NOT NULL,
	NOMBRE VARCHAR(100) NOT NULL,
	CONSTRAINT PK_MARCA PRIMARY KEY(IDMARCA),
	CONSTRAINT U_MARCA_NOMBRE UNIQUE(NOMBRE)
);
