-- Tabla MODELO

CREATE TABLE MODELO(
	IDMODELO INT NOT NULL, -- PRIMARY KEY
	IDMARCA  INT NOT NULL, -- FOREIGN KEY
	NOMBRE   VARCHAR(100) NOT NULL,
	CONSTRAINT PK_MODELO PRIMARY KEY(IDMODELO),
	CONSTRAINT FK_MODELO_MARCA
		FOREIGN KEY(IDMARCA) REFERENCES MARCA
);
GO

